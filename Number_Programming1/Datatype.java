class Datatype
{
	public static void main(String [] args)
	{
		boolean a=true;
		System.out.println(a);

		int b = 2;
		System.out.println(b);

		short e=38;
		System.out.println(e);

		long f=3933;/* L is mandatory at last after data for long */
		System.out.println(f);

		float c=22.9f;/* f is mandatory at last after data for float */
		System.out.println(c);

		double d=38.44;/* d is not mandatory at last after data for float i.e : 38.44d == 38.44(equal)  */
		System.out.println(d);

		char g='A';
		System.out.println(g);

		String h="Divya";/* in String S should be capital and should write in between (" ")double cotes (((mandatory)))*/ 
		System.out.println(h);

		/*System.out.println(boolean.MAX_VALUE);*/

	}
}