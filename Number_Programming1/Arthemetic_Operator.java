class Arthemetic_Operator 
{
	public static void main(String[] args) 
	{
		System.out.println(3+4);//output:7
		System.out.println(3-4);//output:-1
		System.out.println(3*4);//output:12
		System.out.println(17/5);//output:3
		System.out.println(17%5);//output:2
		System.out.println(1/2*10*10);//output:0 (integer divided by integer then it returns interger value only i.e 1/2=0.5 but,it returns 0 (0*10*10=0))
		System.out.println(55.0/100);//output:0.55 (float divided by float then it returns float/deccimal value only i.e 55/100=0.55 )
	
	}
}
